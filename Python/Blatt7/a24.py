lst = [1, 2, 3]
for e in lst:  # aufgabenstellung
    print(e)
i = 0
while i < len(lst):  # lösung
    print(lst[i])
    i += 1
m = [5, 3, 6, 8, 11]
i = 0
while i < len(m):  # aufgabenstellung
    z = m[i]
    print(z, " hoch zwei ist ", z ** 2)
    i += 1
for ele in m:  # lösung
    print(ele, " hoch zwei ist ", ele ** 2)
